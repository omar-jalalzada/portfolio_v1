    <span class="info_title">Eats</span>
    <span class="info_content">
      Eats is a personal project that started from the basic idea of how to keep track of your habits and spendings when it comes to food.
      <br>It worked its way from coffee shop conversations to doodles to sketches and then to full fidelity mock-ups.
      <br>Due to an overloaded schedule and a full time job, my dream of making this a reality is still a dream.
      <br>If your a developer and would like to colaborate, please get in touch.
      <br>
      <br><b>I designed Eats to enable users:</b>
      <br>- Keep track of what you eat and how much you spend on food.
      <br>- Share your story and interact with friends.
      <br>- Explore food in the area through friends. 
      <br>- Stay motivated to eat better, healthier and cheaper.

